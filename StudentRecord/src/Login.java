    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String s=new String(jPasswordField1.getPassword());
        DefaultTableModel dtm=(DefaultTableModel) jTable1.getModel();
        try
        {   Class.forName("java.sql.Driver");
            Connection con1=DriverManager.getConnection("jdbc:mysql://localhost", "root", s);
            JOptionPane.showMessageDialog(null, "Login Successful !!!", "Welcome", JOptionPane.INFORMATION_MESSAGE);
            this.setTitle("Home - Student Record System");
            this.setExtendedState(JFrame.MAXIMIZED_BOTH);
            jLabel4.setText("0");
            jPasswordField1.setEnabled(false);
            jButton1.setEnabled(false);
            jButton2.setVisible(true);
            jButton3.setVisible(true);
            jButton4.setVisible(true);
            jButton5.setVisible(true);
            jButton6.setVisible(true);
            jButton7.setVisible(true);
            jButton8.setVisible(true);
            jButton9.setVisible(true);
            jScrollPane1.setVisible(true);
            jLabel3.setVisible(true);
            jComboBox1.setVisible(true);
            Statement stmt1=con1.createStatement();
            String qry1="show databases;";
            ResultSet rs1=stmt1.executeQuery(qry1);
            int db=0;
            while(rs1.next())
            {   if(rs1.getString(1).equalsIgnoreCase("School"))
                {   db++;
                    break;
                }
            }
            rs1.close();
            stmt1.close();
            if(db==0)
            {   Statement stmt2=con1.createStatement();
                String dbase="create database School;";
                stmt2.executeUpdate(dbase);
                JOptionPane.showMessageDialog(null, "School database not found.\n\nCreated", "Database Error", JOptionPane.INFORMATION_MESSAGE);
                stmt2.close();
            }
            con1.close();
            Connection con2=DriverManager.getConnection("jdbc:mysql://localhost/School", "root", s);
            Statement stmt3=con2.createStatement();
            String qry2="show tables;";
            ResultSet rs2=stmt3.executeQuery(qry2);
            dtm.setRowCount(0);
            jComboBox1.removeAllItems();
            while(rs2.next())
            {   String tab=rs2.getString(1);
                dtm.addRow(new Object[] {tab});
                jComboBox1.addItem(tab);
            }
            if(dtm.getRowCount()==0)
            {   jButton2.setEnabled(false);
                jButton3.setEnabled(false);
                jButton4.setEnabled(false);
                jButton5.setEnabled(false);
                jButton8.setEnabled(false);
                jButton9.setEnabled(false);
                if(db!=0)
                {   JOptionPane.showMessageDialog(null, "No Tables Found !!!", "Table Error", JOptionPane.WARNING_MESSAGE);}
            }
            rs2.close();
            stmt3.close();
            con2.close();
        }
        catch(HeadlessException | ClassNotFoundException | SQLException e)
        {   String err=String.valueOf(Integer.parseInt(jLabel4.getText())+1);
            JOptionPane.showMessageDialog(null, "Incorrect Password\n\nFailure Attempt "+err, "Login Error", JOptionPane.ERROR_MESSAGE);
            jPasswordField1.setText(null);
            jLabel4.setText(err);
            if(Integer.parseInt(err)%5==0)
            {   jPasswordField1.setEnabled(false);
                jButton1.setEnabled(false);
                this.setTitle("Login (Blocked) - Student Record System");
                long t=System.currentTimeMillis();
                if(err.equals("5")||err.equals("10"))
                {   JOptionPane.showMessageDialog(null, "Too many failure login attempts.\n\nBlocking for 20 seconds", "Login Error", JOptionPane.ERROR_MESSAGE);
                    while(System.currentTimeMillis()-t<20000)
                    {}
                }
                else if(err.equals("15"))
                {   JOptionPane.showMessageDialog(null, "Too many failure login attempts.\n\nBlocked", "Login Error", JOptionPane.ERROR_MESSAGE);
                    while(System.currentTimeMillis()-t>=0)
                    {}
                }
                jPasswordField1.setEnabled(true);
                jButton1.setEnabled(true);
                this.setTitle("Login - Student Record System");
                JOptionPane.showMessageDialog(null, "You can now log in", "Login", JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

