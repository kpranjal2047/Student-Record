    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed
        String s=new String(jPasswordField1.getPassword());
        DefaultTableModel dtm=(DefaultTableModel) jTable1.getModel();
        int q=JOptionPane.showConfirmDialog(null, "Are you sure you want to delete the table?", "Delete Table", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        if (q==JOptionPane.YES_OPTION)
        {   String tname=(String) jComboBox1.getSelectedItem();
            try
            {   Class.forName("java.sql.Driver");
                Connection con=DriverManager.getConnection("jdbc:mysql://localhost/School", "root", s);
                Statement stmt1=con.createStatement();
                String qry1="drop table "+tname+";";
                stmt1.executeUpdate(qry1);
                JOptionPane.showMessageDialog(null, "Table Deleted Successfully !!!", "Table", JOptionPane.INFORMATION_MESSAGE);
                stmt1.close();
                Statement stmt2=con.createStatement();
                String qry2="show tables;";
                ResultSet rs=stmt2.executeQuery(qry2);
                dtm.setRowCount(0);
                jComboBox1.removeAllItems();
                while(rs.next())
                {   String tab=rs.getString(1);
                    dtm.addRow(new Object[] {tab});
                    jComboBox1.addItem(tab);
                }
                if(dtm.getRowCount()==0)
                {   jButton2.setEnabled(false);
                    jButton3.setEnabled(false);
                    jButton4.setEnabled(false);
                    jButton5.setEnabled(false);
                    jButton8.setEnabled(false);
                    jButton9.setEnabled(false);
                    JOptionPane.showMessageDialog(null, "No Tables Found !!!", "Table Error", JOptionPane.WARNING_MESSAGE);
                }
                rs.close();
                stmt2.close();
            }
            catch(HeadlessException | ClassNotFoundException | SQLException e)
            {   JOptionPane.showMessageDialog(null, e.getMessage());}
        }
        else
        {   JOptionPane.showMessageDialog(null, "Table not deleted. Aborted by user", "Delete Table", JOptionPane.INFORMATION_MESSAGE);}
    }//GEN-LAST:event_jButton9ActionPerformed

